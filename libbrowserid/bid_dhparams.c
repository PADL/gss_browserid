BIDError
_BIDGetFixedDHParams(
    BIDContext context,
    json_t **pDhParams)
{
    BIDError err;
    json_t *dhParams = NULL;
    static struct BIDDHGroupDesc *dhGroup = NULL;
    size_t i;
    int bFound = 0;

    for (i = 0; i < sizeof(_BIDDHGroups) / sizeof(_BIDDHGroups[0]); i++) {
        dhGroup = &_BIDDHGroups[i];

        if (dhGroup->cbPrime * 8 == context->DHKeySize) {
            bFound++;
            break;
        }
    }

    if (!bFound) {
        err = BID_S_DH_PARAM_GENERATION_FAILURE;
        goto cleanup;
    }

    dhParams = json_object();
    if (dhParams == NULL) {
        err = BID_S_NO_MEMORY;
        goto cleanup;
    }

    err = _BIDJsonObjectSetBinaryValue(context, dhParams, "g",
                                       dhGroup->pbGenerator,
                                       dhGroup->cbGenerator);
    BID_BAIL_ON_ERROR(err);

    err = _BIDJsonObjectSetBinaryValue(context, dhParams, "p",
                                       dhGroup->pbPrime,
                                       dhGroup->cbPrime);
    BID_BAIL_ON_ERROR(err);

    *pDhParams = dhParams;

cleanup:
    if (err != BID_S_OK)
        json_decref(dhParams);

    return err;
}
